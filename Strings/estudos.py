#Estudo de comandos python para Strings.

frase = "Ronaldo é o melhor atacante"  #23 espaços

print(frase[9:21:2]) # O 9:21 ele pega os caractéres do espaço 9 até o 21 o :2 é acrescentado para ele pular de 2 em 2.

print(len(frase)) # Mostra o tamanho de uma string.

frase.count('o') # serve para mostrar quantas vezes tem um determinado caractére em uma string.

frase.find("Ronaldo") # Serve para mostra uma sequência determinada de caractéres.

"Ronaldo" in frase # É um booleano que retorna se existe esses caractéres na variavél.

frase.replace("Ronaldo","Neymar") # Troca as Strings.

frase.upper() # Deixas todos os caractéres em caixa alta.

frase.lower() # Deixas todos os caractéres em caixa baixa.

frase.capitalize() # Irá deixar a string inteira em caixa baixa e o caractére [0] em caixa alta.




